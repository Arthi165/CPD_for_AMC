% Data generation function
function y_vec = data_gen_channel_code_BPSK(G, SNR, num_samples)
    [k, n] = size(G);
    y_vec = zeros(num_samples, n);
    
    for i = 1:num_samples
        m = randi([0, 1], 1, k);
        v = mod(m * G, 2);
        v_prime = 2 * v - 1;
        snr_linear = 10^(SNR / 10);
        noise_power = 1 / snr_linear;
        noise = sqrt(noise_power / 2) * randn(1, n);
        y = v_prime + noise;
        y_vec(i, :) = y;
    end
end

% Decoding function (simple hard-decision decoding for BPSK)
function decoded = decode_BPSK(y, G)
    [~, n] = size(G);
    k = size(G, 1);
    
    % Find closest codeword (minimum distance)
    min_dist = inf;
    decoded = [];
    
    for i = 1:2^k
        m = de2bi(i-1, k, 'left-msb');
        v = mod(m * G, 2);
        v_prime = 2 * v - 1;
        dist = norm(y - v_prime, 'fro');  % Euclidean distance
        
        if dist < min_dist
            min_dist = dist;
            decoded = v;
        end
    end
end
